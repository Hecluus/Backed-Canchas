const { response, request } = require('express');
const Usuario = require('../models/usuario');
const bcrypt = require('bcryptjs');

const usuariosTodosGet = async (req = request, res = response) => {
    const { desde = 0, limite = 5 } = req.query;
    const query = { estado: true };

    const [total, usuarios] = await Promise.all([
        Usuario.countDocuments(query),
        Usuario.find(query).skip(desde).limit(limite)
    ]);

    res.json({
        mensaje: 'Usuarios obtenidos',
        total,
        usuarios
    });
};

const usuarioGetID = async (req = request, res = response) => {
    const { id } = req.params;

    const usuario = await Usuario.findById(id);

    res.json({
        mensaje: 'Usuario obtenido',
        usuario
    });
};

const usuarioPost = async (req = request, res = response) => {
    const datos = req.body;
    const { nombre, apellido, correo, password, rol } = datos;
    const usuario = new Usuario({ nombre, apellido, correo, password, rol });
    const salt = bcrypt.genSaltSync(10);
    const hash = bcrypt.hashSync(password, salt);

    usuario.password = hash;

    await usuario.save();

    res.json({
        mensaje: 'Usuario cargado correctamente',
        usuario
    });
};

const usuarioPut = async (req = request, res = response) => {
    const { id } = req.params;

    const { password, correo, ...resto } = req.body;

    if (password) {
        const salt = bcrypt.genSaltSync(10);
        resto.password = bcrypt.hashSync(password, salt);
    }

    resto.correo = correo;

    const usuario = await Usuario.findByIdAndUpdate(id, resto, { new: true });

    res.json({
        mensaje: 'Usuario actualizado correctamente!',
        usuario
    });
};

const usuarioDelete = async (req = request, res = response) => {
    const { id } = req.params;

    const usuario = await Usuario.findById(id);

    if (!usuario.estado) {
        return res.json({
            mensaje: 'Usuario no existe'
        });
    }

    const usuarioInhabilitado = await Usuario.findByIdAndUpdate(id, { estado: false }, { new: true });

    res.json({
        mensaje: 'Usuarion inhabilitado exitosamente!',
        usuarioInhabilitado
    });
};

module.exports = {
    usuariosTodosGet,
    usuarioGetID,
    usuarioPost,
    usuarioDelete,
    usuarioPut
};
